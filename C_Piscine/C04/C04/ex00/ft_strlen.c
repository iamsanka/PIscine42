#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	char	word[] = "Hello";
	char	*word_p;

	word_p = word;
	printf("characters in the given word is :%d", ft_strlen(word_p));
}
